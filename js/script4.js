// Crea un array di numeri interi e fai la somma di tutti gli elementi che sono in posizione dispari

let numbers = ['30', '24', '27', '82', '59', '8', '16', '21', '44'];

let i = 0;
let sum = 0;

while (i < numbers.length) {

    sum = sum + numbers[i];

    i = 2;

}

